    public void insertDate(){

        try{
            ps = con.prepareStatement("INSERT INTO producto (nombreProducto, precioUnitario, cantidadProducto, fechaVencimiento) VALUES(?,?,?,?)");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtPrecio.getText());
            ps.setString(3, txtCantidad.getText());
            ps.setString(4, txtFecha.getText());
            
            int res = ps.executeUpdate();
            if (res > 0){
                JOptionPane.showMessageDialog(null, "Producto guardado!");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar Producto!");
                limpiar();
            }
            
        }catch(HeadlessException | SQLException e){
            System.err.println(e);
        } finally {
            cn.cerrar();
        }
    }
    
    public  void actualizaDatos(){

        try{
            con = cn.getConnection();
            ps = con.prepareStatement("UPDATE producto SET nombreProducto = ? , precioUnitario = ?, cantidadProducto = ?, fechaVencimiento = ? WHERE codigoProducto = ?");
            int rowSelect = tabla.getSelectedRow();
            String date = (String)tabla.getValueAt(rowSelect, 0);
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtPrecio.getText());
            ps.setString(3, txtCantidad.getText());
            ps.setString(4, txtFecha.getText());
            ps.setString(5, date);
            
            int res = ps.executeUpdate();
            if (res > 0){
                JOptionPane.showMessageDialog(null, "Producto Actualizado!");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Error al actualizar Producto!");
                limpiar();
            }
            
        }catch(HeadlessException | SQLException e){
            System.err.println(e);
        } finally {
            cn.cerrar();
        }
        
    }
    
    public void eliminarDatos(){
            int rowSelect = tabla.getSelectedRow();
        try{
            con = cn.getConnection();
            ps = con.prepareStatement("DELETE FROM producto WHERE codigoProducto = "+tabla.getValueAt(rowSelect, 0));
            int res = ps.executeUpdate();
            if (res > 0){
                JOptionPane.showMessageDialog(null, "Producto Eliminado!");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Error al eliminar Producto!");
                limpiar();
            }
            
        }catch(HeadlessException | SQLException e){
            System.err.println(e);
        } finally {
            cn.cerrar();
        }
    }
        
    public void cargarTabla(){
        String [] encabezado = {"Numero", "Producto", "Precio Unitario", "Cantidad","Vencimiento"};
        String [] contRegistro = new String[10];
        DefaultTableModel modelo = new DefaultTableModel(null, encabezado);
        String Query = ("select * from producto");
        try {
            Statement st = con.createStatement();
            rs = st.executeQuery(Query);
            
            while(rs.next()){
                contRegistro[0] = rs.getString("codigoProducto");
                contRegistro[1] = rs.getString("nombreProducto");
                contRegistro[2] = rs.getString("precioUnitario");
                contRegistro[3] = rs.getString("cantidadProducto");
                contRegistro[4] = rs.getString("fechaVencimiento");
                modelo.addRow(contRegistro);
            }
            
            tabla.setModel(modelo);
        } catch (SQLException e) {
            System.err.println(e);
        }
    } 
    
