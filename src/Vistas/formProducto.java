    public void generarReporteProductosPDF() {
        Document document = new Document();

        try {
            
            String timestamp = new SimpleDateFormat("yyyyMMdd_HHmmss").format(new Date());
            String fileName = "ReporteProductos_" + timestamp + ".pdf";

            
            PdfWriter.getInstance(document, new FileOutputStream(fileName));
            document.open();

           
            Font fontTitulo = new Font(Font.FontFamily.HELVETICA, 18, Font.BOLD);
            Paragraph titulo = new Paragraph("Reporte de Productos", fontTitulo);
            titulo.setAlignment(Element.ALIGN_CENTER);
            document.add(titulo);

            document.add(new Paragraph(" ")); 

            
            PdfPTable table = new PdfPTable(5);
            table.setWidthPercentage(100);

            
            Font fontHeader = new Font(Font.FontFamily.HELVETICA, 12, Font.BOLD);
            String[] headers = {"Numero", "Producto", "Precio Unitario", "Cantidad", "Vencimiento"};

            for (String header : headers) {
                PdfPCell cell = new PdfPCell(new Phrase(header, fontHeader));
                cell.setHorizontalAlignment(Element.ALIGN_CENTER);
                cell.setBackgroundColor(new BaseColor(192, 192, 192)); 
                table.addCell(cell);
            }

            
            for (int i = 0; i < tabla.getRowCount(); i++) {
                for (int j = 0; j < tabla.getColumnCount(); j++) {
                    table.addCell(new PdfPCell(new Phrase(tabla.getValueAt(i, j).toString())));
                }
            }

            document.add(table); 
            JOptionPane.showMessageDialog(null, "Reporte de productos generado correctamente: " + fileName);

        } catch (DocumentException | IOException ex) {
            System.err.println("Error al generar PDF: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Error al generar reporte de productos.");
        } finally {
            document.close();
        }
    }

